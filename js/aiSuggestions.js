export const AI_QUICK_ACTIONS = [
    "Открыть кассу",
    "Создать товар",
    "Создать закупку",
    "Покажи проблемы в заведении",
];

export function getSuggestions(context) {
    if (context.currentView === "pos") {
        return ["Как быстрее оформить чек?", "Что чаще всего продается?", "Помоги найти товар"];
    }

    if (context.currentView === "floor") {
        return ["Как создать бронь?", "Покажи занятые столы", "Что делать с активным заказом?"];
    }

    if (context.currentView === "kitchen") {
        return ["Покажи просроченные заказы", "Какие блюда готовятся дольше всего?", "Как ускорить кухню сегодня?"];
    }

    if (context.currentView === "warehouse") {
        return ["Какие продукты заканчиваются?", "Что нужно заказать сегодня?", "Покажи ингредиенты ниже минимума"];
    }

    if (context.currentView === "menu") {
        return ["Создать первое меню", "Импортировать меню", "Создай категорию Десерты", "Добавь меню кофейни"];
    }

    if (context.currentView === "clients") {
        return ["Добавить первого клиента", "Настроить бонусы", "Создать программу лояльности", "Кто не приходил 60 дней?"];
    }

    if (context.currentView === "staff") {
        return ["Добавь нового официанта", "Кто сейчас работает?", "Создай смену на завтра"];
    }

    return ["Сколько продаж сегодня?", "Открой кассу", "Покажи проблемы в заведении"];
}
